      private void LoadData() {
    try {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Mã dịch vụ");
        model.addColumn("Tên dịch vụ");
        model.addColumn("Đơn giá");
      
        List<DichVu> dichVus = dichVuDao.getListDichVu();

        
        for (DichVu c : dichVus) {
            Object[] row = {
                c.getMaDichVu(),
                c.getTenDichVu(),
                c.getGiaDichVu()
            };
            model.addRow(row);
        }

        // Set model cho jTable1
        jTable1.setModel(model);
    } catch (Exception ex) {
        ex.printStackTrace();
        // Xử lý lỗi nếu cần
    }
    txt_madv.setText("");
    txt_tendv.setText("");
    txt_dongia.setText("");
  
   
    
}

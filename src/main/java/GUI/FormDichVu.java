    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (txt_madv.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn dịch vụ cần sửa!");
            return;
        }
        if(dichVuDao.edit(new DichVu(Integer.parseInt(txt_madv.getText()), txt_tendv.getText(), Double.parseDouble(txt_dongia.getText())))){
            LoadData();
            JOptionPane.showMessageDialog(rootPane, "Sửa thành công");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Sửa thất bại");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (txt_madv.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn dịch vụ cần xóa!");
            return;
        }
        if(dichVuDao.delete(new DichVu(Integer.parseInt(txt_madv.getText()), txt_tendv.getText(), Double.parseDouble(txt_dongia.getText())))){
            LoadData();
            JOptionPane.showMessageDialog(rootPane, "Xóa thành công");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Xóa thất bại");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            Integer maPhong = (Integer) jTable1.getValueAt(selectedRow, 0);
            String tenPhong = (String) jTable1.getValueAt(selectedRow, 1);
            Double moTa = (Double) jTable1.getValueAt(selectedRow, 2);

            // Gán giá trị vào các ô text
            txt_madv.setText(maPhong.toString());
            txt_tendv.setText(tenPhong);
            txt_dongia.setText(moTa.toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (txt_tendv.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập tên dv!");
            return;
        }
         if (txt_dongia.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đơn giá!");
            return;
        }
        if(dichVuDao.add(new DichVu(0, txt_tendv.getText(), Double.parseDouble(txt_dongia.getText())))){
            LoadData();
            JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


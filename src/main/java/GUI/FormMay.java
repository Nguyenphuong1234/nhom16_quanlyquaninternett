    private void LoadData() {
    try {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Mã máy");
        model.addColumn("Trạng thái");
        model.addColumn("Thông số");
        model.addColumn("Giá theo giờ");
        model.addColumn("Phòng");
        
      
        List<May> mays = mayDao.getListMay();

        
        for (May c : mays) {
            Object[] row = {
                c.getMaMay(),
                c.getTrangThai(),
                c.getThongSo(),
                c.getGiaTheoGio(),
                phongDao.getTenPhongById(c.getMaPhong())
            };
            model.addRow(row);
        }

        // Set model cho jTable1
        jTable1.setModel(model);
    } catch (Exception ex) {
        ex.printStackTrace();
        // Xử lý lỗi nếu cần
    }
    txt_mamay.setText("");
   
    txt_thongso.setText("");
    txt_dongia.setText("");
  
}
    public void loadPhong() {
       
        List<Phong> phongs = phongDao.getListPhong();
        DefaultComboBoxModel<Phong> spModel = new DefaultComboBoxModel<>(phongs.toArray(new Phong[0]));
        cbx_phong.setModel(spModel);  
    }

   
    private int getMaPhong() {
        Phong selected = (Phong) cbx_phong.getSelectedItem();
        if (selected != null) {
            return selected.getMaPhong();
        }
        return -1;
    }

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            Integer maMay = (Integer) jTable1.getValueAt(selectedRow, 0);
           
            String thongSo = (String) jTable1.getValueAt(selectedRow, 2);
            Double donGia = (Double) jTable1.getValueAt(selectedRow, 3);
         
            
            
            // Gán giá trị vào các ô text
            txt_mamay.setText(maMay.toString());
         
            txt_thongso.setText(thongSo);
            txt_dongia.setText(donGia.toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked


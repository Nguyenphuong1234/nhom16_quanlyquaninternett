    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (txt_tenphong.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập tên phòng!");
            return;
        }
        if(phongDao.add(new Phong(0, txt_tenphong.getText(), txt_mota.getText()))){
            LoadData();
            JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (txt_maphong.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn phòng cần sửa!");
            return;
        }
         if(phongDao.edit(new Phong(Integer.parseInt(txt_maphong.getText()), txt_tenphong.getText(), txt_mota.getText()))){
            LoadData();
            JOptionPane.showMessageDialog(rootPane, "Sửa thành công");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Sửa thất bại");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            Integer maPhong = (Integer) jTable1.getValueAt(selectedRow, 0);
            String tenPhong = (String) jTable1.getValueAt(selectedRow, 1);
            String moTa = (String) jTable1.getValueAt(selectedRow, 2);
            
            
            // Gán giá trị vào các ô text
            txt_maphong.setText(maPhong.toString());
            txt_tenphong.setText(tenPhong);
            txt_mota.setText(moTa);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (txt_maphong.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn phòng cần xóa!");
            return;
        }
         if(phongDao.delete(new Phong(Integer.parseInt(txt_maphong.getText()), txt_tenphong.getText(), txt_mota.getText()))){
            LoadData();
            JOptionPane.showMessageDialog(rootPane, "Xóa thành công");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Xóa thất bại");
        }
    }//GEN-LAST:event_jButton3ActionPerformed


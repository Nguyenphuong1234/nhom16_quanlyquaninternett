    private void LoadData() {
    try {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Mã phòng");
        model.addColumn("Tên phòng");
        model.addColumn("Mô tả");
      
        List<Phong> phongs = phongDao.getListPhong();

        
        for (Phong c : phongs) {
            Object[] row = {
                c.getMaPhong(),
                c.getTenPhong(),
                c.getMoTa()
            };
            model.addRow(row);
        }

        // Set model cho jTable1
        jTable1.setModel(model);
    } catch (Exception ex) {
        ex.printStackTrace();
        // Xử lý lỗi nếu cần
    }
    txt_maphong.setText("");
    txt_tenphong.setText("");
    txt_mota.setText("");
  
   
    
}
